CREATE TABLE empresa (
    id char(36) PRIMARY KEY,
    nomerazao VARCHAR(250),
    apelidofantasia VARCHAR(250),
    cpfcnpj VARCHAR(250),
    bairro VARCHAR(250),
    cep VARCHAR(250),
    cidade VARCHAR(250),
    pais VARCHAR(250),
    estado VARCHAR(250),
    logradouro VARCHAR(1000),
    contato01 VARCHAR(250),
    contato02 VARCHAR(250),
    email VARCHAR(250),
    emailFinanceiro VARCHAR(250)
);